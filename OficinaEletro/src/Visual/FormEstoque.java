    private void btnExcluirEstoqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirEstoqueActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showOptionDialog(null, "Confirma a exclusão?", 
                 "Pergunta",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, 
                 null, new String [] {"Sim","Não"},"Sim");     
         if(opcao==0){    
             int linhaSelecionada = tblEstoque.getSelectedRow();         
             Estoque obj = listaEstoque.get(linhaSelecionada);        
             daoEstoque.remover(obj);        
             atualizaTabela();   
             trataEdicao(false);      
         } 
    }//GEN-LAST:event_btnExcluirEstoqueActionPerformed

