    public boolean validaCampos() {
        if (!(txtNomeEstoque.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe o nome da peça");
            txtNomeEstoque.requestFocus();
            return false;
        }
        if (!(txtValorEstoque.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe o valor da peça");
            txtValorEstoque.requestFocus();
            return false;
        }
        if (!(txtQuantidadeEstoque.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe a quantidade da peça");
            txtQuantidadeEstoque.requestFocus();
            return false;
        }
        return true;
    }
    
    private void trataEdicao(boolean editando) {
        btnCancelarEstoque.setEnabled(editando);
        btnSalvarEstoque.setEnabled(editando);
        btnEditarEstoque.setEnabled(!editando);
        int linha = listaEstoque.size() - 1;
        if (linha < 0) {
            btnExcluirEstoque.setEnabled(false);
            txtCodigoEstoque.setText("");
            txtNomeEstoque.setText("");
            txtValorEstoque.setText("");
            txtQuantidadeEstoque.setText("");
        } else {
            btnExcluirEstoque.setEnabled(!editando);
        }
        btnExcluirEstoque.setEnabled(!editando);
        btnNovoEstoque.setEnabled(!editando);
        btnFecharEstoque.setEnabled(!editando);
        btnPrimeiroEstoque.setEnabled(!editando);
        btnProximoEstoque.setEnabled(!editando);
        btnAnteriorEstoque.setEnabled(!editando);
        btnUltimoEstoque.setEnabled(!editando);
        txtNomeEstoque.setEnabled(editando);
        txtValorEstoque.setEnabled(editando);
        txtQuantidadeEstoque.setEnabled(editando);
        tblEstoque.setEnabled(editando);
    }
    
    public void atualizaTabela() {
        listaEstoque.clear();
        listaEstoque.addAll(daoEstoque.getLista());
        int linha = listaEstoque.size() - 1;
        if (linha >= 0) {
            tblEstoque.setRowSelectionInterval(linha, linha);
            tblEstoque.scrollRectToVisible(tblEstoque.getCellRect(linha, linha, true));
        }
    }
    
